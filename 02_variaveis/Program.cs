/*
    Vamos utilizar variáveis para as seguintes situações:
    - Textos
    - Letras
    - Números inteiros
    - Números reais
    - Lógicos
*/

// Criando as variáveis
// Textos
string nome = "Gilmar";
// Letras
char letra = 'G'; // para char usamos apóstrofos
// Números inteiros
int idade = 40;
// Números reais
double altura = 1.79;
// Booleanos(verdadeiro ou falso)
bool estuda = true;

// Variáveis criadas, agora vamos exibir seus valores
Console.WriteLine(nome);
Console.WriteLine(letra);
Console.WriteLine(idade);
Console.WriteLine(altura);
Console.WriteLine(estuda);

