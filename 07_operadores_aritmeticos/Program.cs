int n1 = 8;
int n2 = 2;

// Soma
Console.WriteLine(n1 + n2);

// Subtração
Console.WriteLine(n1 - n2);

// Multiplicação
Console.WriteLine(n1 * n2);

// Divisão
Console.WriteLine(n1 / n2);

// Resto da Divisão
Console.WriteLine(n1 % n2);

// Incremento
Console.WriteLine(++n1);

// Decremento
Console.WriteLine(--n2);
 