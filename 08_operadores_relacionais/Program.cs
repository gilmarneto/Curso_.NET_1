int n1 = 8;
int n2 = 3;

// Operadores Relacionais

// menor que
Console.WriteLine(n1 < n2);

// maior que
Console.WriteLine(n1 > n2);

// menor igual à
Console.WriteLine(n1 <= n2);

// maior igual à
Console.WriteLine(n1 >= n2);

// igual à
Console.WriteLine(n1 == n2);

// diferente de
Console.WriteLine(n1 != n2);